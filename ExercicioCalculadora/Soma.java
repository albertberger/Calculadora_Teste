package ExercicioCalculadora;
/**
 * classe utilizada para somar
 */
public class Soma {
	/**
	 * regra de negocio somar 
	 * 
	 * @param A numero a ser somado 
	 * @param B numero somador
	 * @return {@code int} resultado da soma de A por B
	 */
    public int somar (int A, int B){
        return A + B;
    }
}
